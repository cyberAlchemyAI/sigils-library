#!/usr/bin/env bash
set -euo pipefail

usage() {
	cat <<'USAGE'
Usage:
  reflect-loop-attempt.sh <artifact-path> <regime-id> <attempt-dir>
USAGE
}

if [[ "$#" -ne 3 ]]; then
	usage >&2
	exit 2
fi

artifact="$1"
regime_id="$2"
attempt_dir="$3"

artifact_abs="$(cd "$artifact" && pwd)"
repo_root="${EXPERIMENT_REPO_ROOT:-$(git -C "$PWD" rev-parse --show-toplevel 2>/dev/null || pwd)}"
validation="$attempt_dir/validation.json"
robot_talks="$attempt_dir/robot-talks.md"
argument="$attempt_dir/improvement-argument.md"

if [[ ! -f "$validation" ]]; then
	printf 'ERROR: validation not found: %s\n' "$validation" >&2
	exit 1
fi

status="$(jq -r '.status' "$validation")"
missing="$(jq -r '.missing_patterns | if length == 0 then "none" else join("; ") end' "$validation")"
quality="$(jq -r '.quality_bar_status' "$validation")"
anti="$(jq -r '.anti_pattern_hits | if length == 0 then "none" else join("; ") end' "$validation")"

cat > "$robot_talks" <<EOF
# Robot-Talks: $regime_id Attempt Reflection

## Central Question

Why did this experiment attempt fail to reach a stable passing output, and what bounded improvement should be tried next?

## Participant Turns

### Contract Reviewer

- Perspective: output-contract and regime-fit reviewer.
- Finding: attempt status is \`$status\`.
- Evidence: \`${validation#$repo_root/}\`.
- Gap: missing required patterns: $missing.

### Quality Reviewer

- Perspective: Quality Bar and Anti-Pattern reviewer.
- Finding: Quality Bar status is \`$quality\`.
- Finding: Anti-Pattern hits: $anti.
- Evidence: \`${validation#$repo_root/}\`.

### Rollback Reviewer

- Perspective: regression and rollback safety reviewer.
- Finding: any improvement must be reversible and must not edit generated outputs.
- Evidence: loop-first architecture rollback rule.

## Tensions

- The loop needs a better next attempt, but generated output must remain evidence and cannot be edited to force a pass.
- Auto-improvement is allowed, but the improvement must target prompt, validator, or artifact contract causes and be reversible.

## Synthesis

The next improvement should address the missing required patterns or contract drift directly. If no bounded patch is available, the safe next action is to retry without patching or stop with the recorded failure.
EOF

cat > "$argument" <<EOF
# Improvement Argument: $regime_id

## Failure Summary

- Attempt status: \`$status\`
- Missing patterns: $missing
- Quality Bar status: \`$quality\`
- Anti-Pattern hits: $anti

## Tensions Found

- The output must improve, but generated evidence cannot be edited.
- The artifact may be improved, but only through a reversible patch.

## Proposed Patch

No deterministic patch is generated by this reflector.

## Why This Should Improve The Next Run

If a maintainer adds a bounded patch based on the missing patterns above, the next live run should have clearer instructions or stronger validation alignment. Without a bounded patch, retrying can still expose whether the failure was model variance.

## Files To Change

- none

## Regression Criteria

The next run is worse if validation severity increases, anti-pattern severity increases, output becomes empty or save-summary, required regime sections disappear, or observability disappears.

## Rollback Plan

If a patch is added later, capture \`patch.diff\` before rerun and apply it in reverse if the next attempt regresses.
EOF

printf 'ROBOT_TALKS=%s\n' "${robot_talks#$repo_root/}"
printf 'IMPROVEMENT_ARGUMENT=%s\n' "${argument#$repo_root/}"
